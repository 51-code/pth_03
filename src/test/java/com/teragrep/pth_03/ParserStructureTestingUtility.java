package com.teragrep.pth_03;

import com.teragrep.pth_03.ParseTreeToXml;
import com.teragrep.pth_03.antlr.DPLLexer;
import org.antlr.v4.runtime.CharStream;
import org.antlr.v4.runtime.CharStreams;
import org.antlr.v4.runtime.CommonTokenStream;
import org.antlr.v4.runtime.tree.ParseTree;
import org.w3c.dom.Document;
import org.w3c.dom.NodeList;

import javax.xml.transform.OutputKeys;
import javax.xml.transform.Transformer;
import javax.xml.transform.TransformerFactory;
import javax.xml.transform.dom.DOMSource;
import javax.xml.transform.stream.StreamResult;
import javax.xml.xpath.XPath;
import javax.xml.xpath.XPathConstants;
import javax.xml.xpath.XPathExpression;
import javax.xml.xpath.XPathFactory;

import com.teragrep.pth_03.antlr.DPLLexer;
import com.teragrep.pth_03.antlr.DPLParser;

import java.io.File;
import java.nio.charset.StandardCharsets;
import java.nio.file.Files;
import java.nio.file.Paths;

import static org.junit.jupiter.api.Assertions.assertEquals;

public class ParserStructureTestingUtility {
    public ParserStructureTestingUtility() {

    }

    public Object xpathQuery(String dplQuery, String xPathExpression, Boolean printDom) throws Exception {
        CharStream inputStream = CharStreams.fromString(dplQuery);
        DPLLexer lexer = new DPLLexer(inputStream);
        DPLParser parser = new DPLParser(new CommonTokenStream(lexer));
        ParseTree tree = parser.root();
        ParseTreeToXml parseTreeAsXml = new ParseTreeToXml(parser, tree);
        Document domDocument = parseTreeAsXml.asDomDocument();

        if (printDom) {
            // print out the document for debugging
            TransformerFactory transformerFactory = TransformerFactory.newInstance();
            Transformer transformer = transformerFactory.newTransformer();
            transformer.setOutputProperty(OutputKeys.ENCODING, "UTF-8");
            transformer.setOutputProperty(OutputKeys.INDENT, "yes");
            DOMSource source = new DOMSource(domDocument);
            StreamResult result =  new StreamResult(System.out);
            transformer.transform(source, result);
        }

        // Use Xpath to select
        XPath xPath = XPathFactory.newInstance().newXPath();
        XPathExpression expression = xPath.compile(xPathExpression);
        return expression.evaluate(domDocument, XPathConstants.NODESET);
    }

    public Object xpathQueryFile(String path, String xPathExpression, Boolean printDom) throws Exception {
        String query = new String(Files.readAllBytes(Paths.get(path)), StandardCharsets.UTF_8);
        return xpathQuery(query, xPathExpression, printDom);
    }
}
